	<h2 class="text-center text-2xl mb-6">Our Lineages</h2>

	<h3 class="text-center text-xl mb-4">The Shangpa Kagyu lineage</h3>

	<p class="mb-2 text-lg">
		The Shangpa Kagyu lineage is often referred to as the <span class="italic">secret lineage</span>
		because Niguma instructed Khyungpo Naljor to transmit the teachings to only one student for the first
		seven generations (beginning with the Buddha Vajradhara and Niguma).
	</p>
	<p class="mb-2 text-lg">
		From Gyalse Mok Chokpa, the lineage was passed to Kyergang Chokyi Senge (Kyergangpa) , Nyentön
		Rigung Chökyi Sherab(Rigongpa), and Sangye Töngpa. The first seven masters are known as the
		Seven Great Jewels of the Shangpa tradition. Sangye Töngpa was the first teacher who gave these
		instructions to more than one of his disciples, and from this point on, several different lines
		of transmission developed. The intention for keeping the lineage secret in this fashion was to
		protect it from becoming an established monastic tradition. His Eminence The 2nd Kalu Rinpoche,
		is the current head lineage holder of the Shangpa Kagyu tradition.
	</p>
	<p class="mb-2 text-lg">
		As one of the more esoteric traditions, it is meant to be practiced rather than codified.
	</p>

	<div class="italic text-right mb-8">
		from “Shangpa Kagyu Teachings” by Chamgon Kenting Tai Situpa Kagyu
	</div>

	<h3 class="text-center text-xl mb-4">Karma Kagyu lineage</h3>

	<p class="mb-6 text-lg">
		The Karma Kagyu lineage is headed by His Holiness The 17th Karmapa, Ogyen Trinley Dorje. The
		Lord Buddha Shakyamuni foretold the coming of the lineage of the Karmapas. Karmapa means “the
		one who carries out buddha-activity” or “the embodiment of all the activities of the buddhas.”
		The lineage began with the primordial deity, Vajradhara (Dorje Chang in Tibetan). He passed the
		teachings down to Tilopa, a Mahasiddha from India. Tilopa taught Naropa, a scholar, who became
		enlightened through the teachings of Tilopa. Then a linguist from Tibet was interested, and he
		went to great lengths to find and learn from Naropa. His name was Marpa, and he took the
		teachings back to Tibet, translated them into Tibetan, and began to teach. His primary student
		was Milarepa, the poet-saint who wore only a thin cotton shawl and lived in caves. Milarepa had
		many students, but he passed the teachings to Gampopa, who wrote The Jewel Ornament of
		Liberation, which is still in print. Gampopa chose to pass the teachings to The First Karmapa,
		Dusum Khyenpa. Dusum Khyenpa vowed to reincarnate as the lineage holder of the Karma Kagyu
		Lineage for 25 incarnations and His Holiness the 17th Karmapa continues the Buddha activity of
		the lineage to this day.
	</p>
